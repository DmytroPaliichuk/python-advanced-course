"""
1. Високонавантажений REST API для каталогу товарів

Cache-aside (read)

Чому:
    - Дані читаються часто, змінюються рідко.
    - Потрібно контролювати інвалідацію вручну після змін.
    - Кеш повинен оновлюватися лише на вимогу, а не автоматично.
    - Дає можливість мати оперативний кеш без ризику повернути застарілі дані після оновлення.
"""

"""
2. Пайплайн обробки подій

Cache-behind

Чому:
	- Можна відкладати запис у БД (пакетний запис).
	- Події можуть потрапити у БД із затримкою - це допустимо.
	- Основна вимога - не втратити дані -> бекграундний механізм запису підходить.
	- Зменшується навантаження на БД, що відповідає обмеженню клієнта.
"""

"""
3. Платформа для рекомендацій на основі LLM

Cache-through (read-through)

Чому:
	- Якщо немає в кеші - система сама оновлює кеш через бекенд.
	- Забезпечує актуальність після оновлення профілю.
	- Не дозволяє повертати stale дані, оскільки оновлення проходить через один шар.
	- Підходить, коли генерація даних дорога, і кеш має бути "завжди заповнений".
"""

"""
4. Фінансовий сервіс, що виконує транзакції

Cache-through (write-through)

Чому:
	- Запис у БД і кеш відбувається синхронно.
	- Немає ризику розсинхронізації балансу.
	- Атомарність важливіша за продуктивність -> це найбільш консистентна стратегія.
	- Підходить для грошей, балансів, лімітів.
"""

"""
5. CDN-like сервіс для контенту

Cache-ahead (pre-warm)

Чому:
	- Потрібно наперед заповнити кеш до піку трафіку.
	- Статичні, великі об’єкти - ідеальний кейс.
	- Підходить для "майбутніх" звернень: прогріти сторінки, файли, зображення.
"""

"""
6. Система обліку замовлень у логістичній компанії

Cache-aside (write-aside)

Чому:
	- Записи не обов’язково одразу мають бути в кеші.
	- Можна скидати оновлення в БД напряму, а кеш оновлювати лише при читанні.
	- Підходить, коли write-heavy і read-light.
	- Дозволяє виносити частину навантаження з БД, але не блокує записів.
"""
